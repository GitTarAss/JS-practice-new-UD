'use strict'; // Режим сучасного кода!

// Интерполяция строк — это процесс замены заполнителей в строке значениями строковой переменной

const category = 'toys';

console.log(`https://www.google.com.ua/${category}/5`);
// Для интерполяции нужно прописывать код в косых кавычках `...` - (backtick)
// Потом знак ${...} Результат в консоли => https://www.google.com.ua/toys/5


const user = "Ivan";

